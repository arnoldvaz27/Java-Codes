import java.util.Scanner;

class SwitchCaseCheckin
{
	public static void main(String args[])
	{
		char a='B';
		switch(a)
		{
			case 'A' : 
			case 'E' : 
			case 'I' : 
			case 'O' : 
			case 'U' : System.out.println("Vowel");
			break;
			default  : System.out.println("Not Vowel");
			
		}		
	}	

}